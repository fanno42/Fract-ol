#include "libft.h"

void	ft_strmerge(char **s1, char **s2)
{
	char	*buffer;

	if (*s1 == NULL)
		*s1 = *s2;
	else
	{
		buffer = ft_strjoin(*s1, *s2);
		free(*s1);
		free(*s2);
		*s1 = buffer;
	}
}
